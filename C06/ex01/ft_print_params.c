#include <unistd.h>

int	main(int argc, char **argv)
{
	int	n;

	n = 1;
	while (n < argc)
	{
		while (*argv[n])
		{
			write(1, argv[n], 1);
			argv[n]++;
		}
		n++;
		write(1, "\n", 1);
	}
	return (0);
}
